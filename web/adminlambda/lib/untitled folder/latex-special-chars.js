function applyLatexReplacements(input, replacements, commandMode) {
    var activeReplacements = replacements
        ? replacements.map(function (item) {
            return [commandMode ? "command" : "literal", item[0], item[1]];
        })
        : LATEX_REPLACEMENTS;

    return activeReplacements
        .slice()
        .sort(function (left, right) {
            if (left[0] !== right[0]) {
                return left[0] === "literal" ? -1 : 1;
            }
            return right[1].localeCompare(left[1]);
        })
        .reduce(function (text, item) {
            var mode = item[0];
            var token = item[1].replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
            var pattern = mode === "command" ? "\\\\" + token + "(?![A-Za-z])" : token;
            return text.replace(new RegExp(pattern, "g"), item[2]);
        }, input);
}

